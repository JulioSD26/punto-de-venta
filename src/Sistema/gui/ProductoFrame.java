    private void btnGuardarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarProductoActionPerformed
        // capturar datos
        String codigo = campoClave.getText();
        String nombre = campoNombre.getText();
        String descripcion = campoDesc.getText();
        double stock = Double.parseDouble(campoStock.getText());
        double precioCompra = Double.parseDouble(campoPrecioCompra.getText());
        double precioVenta = Double.parseDouble(campoPrecioVenta.getText());
        String unidad = comboUnidades.getSelectedItem().toString();
        CategoriaProd categoria = (CategoriaProd)comboCategorias.getSelectedItem();
        Proveedor proveedor = (Proveedor)comboProveedores.getSelectedItem();
        
        if (estaActualizando) {
            if (imgArticleFile == null) {
                Producto producto = new Producto(codigo, nombre, descripcion, stock, null, unidad, precioCompra, precioVenta, 0.0, categoria.getIdCategoriaProd(),
                proveedor.getIdProveedor());
                
                base.actualizarProducto(producto, false);
            } else{
                Producto producto = new Producto(codigo, nombre, descripcion, stock, imgArticleFile, unidad, precioCompra, precioVenta,0.0, categoria.getIdCategoriaProd(),
                proveedor.getIdProveedor());
                
                base.actualizarProducto(producto, true);
            }
            
            JOptionPane.showMessageDialog(this, "Se ha guardado el producto");
            this.dispose();
            informacion = "1";
            
            if (imgArticleFile == null) {
                JOptionPane.showMessageDialog(this, "No ha elegido la fotografia del producto");
            }else{
                Producto producto = new Producto(codigo, nombre, descripcion, stock, imgArticleFile, unidad, precioCompra, precioVenta, 0.0, categoria.getIdCategoriaProd(), proveedor.getIdProveedor());
                base.insertarProducto(producto);
                
                JOptionPane.showMessageDialog(this, "Se ha guardado el producto");
                this.dispose();
            }
        } else{
            System.out.println("Error en guardar producto");
        }
        
    }//GEN-LAST:event_btnGuardarProductoActionPerformed

